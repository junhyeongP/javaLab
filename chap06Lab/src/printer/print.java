package printer;

public class print {
	void println(int val) {
		System.out.println(val);
	}
	//메소드 오버로딩(method overloading)
	void println(boolean val) {
		System.out.println(val);
	}
	void println(double val) {
		System.out.println(val);
	}
	void println(String val) {
		System.out.println(val);
	}
	

}
